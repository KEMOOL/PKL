    Kantor Perpustakaan Dan Arsip Daerah
    ( Sumber : Peraturan Bupati Semarang Nomor. 91 th 2011 Tentang Tugas Pokok dan Fungsi, dan Rincian Tugas BAPPEDA, INSPEKTORAT, Lembaga Teknis Daerah, Kantor Penanaman Modal dan Perijinan Terpadu, dan Badan Penanggulangan Bencana Daerah Kabupaten Semarang )


    Kepala Kantor Perpustakaan Dan Arsip Daerah
    Melaksanakan penyusunan dan pelaksanaan kebijakan daerah di bidang perpustakaan dan kearsipan.


    Sub Bagian Tata Usaha
    Melaksakan sebagian tugas Kepala Kantor Perpustakaan dan Arsip Daerah dalam penyusunan perencanaan, administrasi umum, administrasi kepegawaian, dan keuangan.


    Seksi Pengelolaan Perpustakaan
    Melaksakan sebagian tugas Kepala Kantor Perpustakaan dan Arsip Daerah di bidang pengelolaan perpustakaan


    Seksi Pengelolaan Kearsipan
    Melaksakan sebagian tugas Kepala Kantor Perpustakaan dan Arsip Daerah di bidang pengelolaan kearsipan



    Seksi Pengembangan Perpustakaan dan Kearsipan

    Melaksakan sebagian tugas Kepala Kantor Perpustakaan dan Arsip Daerah di bidang pengembangan perpustakaan dan kearsipan
